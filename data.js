/* ── Agrupación de temas por colección (fuente única de verdad) ── */
const collectionGroups = {
  "Micología General": [
    "Micología Básica", "Taxonomía", "Ecología",
    "Genética", "Hongos Comestibles", "Toxicología y Micopatología"
  ],
  "Cultivo & Biotecnología": [
    "Cultivo e Inóculo", "Biotecnología", "Micoterapia"
  ]
};

/* ── Metadatos de colección + descripción ── */
const collectionInfo = {
  "Micología General": {
    emoji: "🍄",
    desc: "Aquí encontrarás recursos sobre biología fúngica, taxonomía, ecología, fisiología y métodos de estudio del reino Fungi."
  },
  "Cultivo & Biotecnología": {
    emoji: "⚙️",
    desc: "Aquí encontrarás recursos sobre micología aplicada, fermentación, industrialización y bioquímica de hongos."
  }
};

/* ── collectionMeta se calcula a partir de categoryMeta: cero duplicación ── */
const collectionMeta = Object.keys(collectionGroups).reduce(function (acc, colName) {
  const rawResources = collectionGroups[colName].reduce(function (list, theme) {
    return list.concat((categoryMeta[theme] && categoryMeta[theme].resources) || []);
  }, []);

  // Desduplicar recursos por título dentro de la colección
  const seen = new Set();
  const uniqueResources = [];
  rawResources.forEach(function (res) {
    const key = res.title.trim().toLowerCase();
    if (!seen.has(key)) {
      seen.add(key);
      uniqueResources.push(res);
    }
  });

  acc[colName] = {
    emoji: collectionInfo[colName].emoji,
    desc: collectionInfo[colName].desc,
    resources: uniqueResources
  };
  return acc;
}, {});


